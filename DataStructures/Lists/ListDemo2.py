# creating list of different type
student_details = ["Jack", 101321, "Information Science"]
print(student_details)
print(student_details[0])
print(student_details[1])
print(student_details[2])

# converting to list using list()
x = "Orange"
colors_list = list(x)
print(colors_list)

# list can contain duplicates
print("Student Names")
student_names = ["Jack", "Mahender", "Jack", "Suresh", "Chandan"]
print(student_names)

# Negative indexing in lists
colors = ["Red", "Orange", "Blue", "Yellow"]
print(colors)
print(colors[-1])
print(colors[-2])
print(colors[-3])

cars = ['BMW', 'Mercedes', 'Tesla']
print('Total Elements: ', len(cars))
# Output: Total Elements:  3
